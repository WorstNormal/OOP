package ru.nsu.gaev;

/**
 * Интерфейс для представления оценки за предмет.
 * Позволяет работать с разными типами оценок единообразно.
 */
public interface Grade {
    /**
     * Возвращает числовое представление оценки.
     * Для зачетов: 0 - не зачет, 1 - зачет.
     * Для дифференцированных оценок: 2-5.
     *
     * @return числовое значение оценки
     */
    int getValue();

    /**
     * Возвращает строковое представление оценки.
     *
     * @return строка с описанием оценки
     */
    String getDisplayName();
}

